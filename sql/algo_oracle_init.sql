  CREATE OR REPLACE FUNCTION "GRADE" (
  p_custid Customer.custid%TYPE
) RETURN VARCHAR2
IS 
l_grade VARCHAR(6);
l_sum NUMBER;
BEGIN
  SELECT SUM(saleprice)  INTO l_sum
    FROM Orders 
    where custid = p_custid;
  IF l_sum >=100 THEN
    l_grade := '우수';
  ELSE 
    l_grade := '일반';
  END IF;
  RETURN l_grade;
END;




